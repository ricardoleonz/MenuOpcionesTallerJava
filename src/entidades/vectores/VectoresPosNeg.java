package entidades.vectores;

public class VectoresPosNeg {

    private int [] vecPos;
    private int [] vecNeg;


    public void setVecPos(int[] vecPos) {

        this.vecPos = vecPos;
    }

    public int[] getVecPos() {

        return vecPos;
    }

    public void setVecNeg(int[] vecNeg) {

        this.vecNeg = vecNeg;
    }

    public int[] getVecNeg() {

        return vecNeg;
    }

}
